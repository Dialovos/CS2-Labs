namespace PolymorphismLab
{
    /// <summary>
    /// existing printer class
    /// </summary>
    public class OldPrinter
    {
        /// <summary>
        /// Prints text using the old printer
        /// </summary>
        /// <param name="text"></param>
        public void PrintOld(string text)
        {
            Console.WriteLine($"Old Printer: {text}");
        }
    }
}